CREATE TABLE IF NOT EXISTS "order".orders (

order_id UUID PRIMARY KEY,
user_id UUID,
status VARCHAR(30) NOT NULL,
total_amount DECIMAL,
created_at TIMESTAMP DEFAULT NOW(),
updated_at TIMESTAMP DEFAULT NOW()

);

